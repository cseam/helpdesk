<h3>Adding a ticket</h3>
<p>Please use the form to add a ticket/job for engineers, when you create a ticket you can select which department you want to report the problem with.</p>
<p>Try to provide as much usefull information and include pictures where posible to aid resolution of the problem, once your ticket has been created your will receive email updates as the engineer works on the task.</p>
<p class="note">
  Remember, the more information you provide, the quicker the engineer can fix your problem.<br/> For example, if your printer is out of ink, include the location of the printer, the printer model, the colour of the ink cartridge, etc. <br/> This saves the engineer asking these basic questions as the first thing and will speed up your ticket.
</p>
<p class="note">
  When completing the form please enter your name and contact email, not a generic house or department account; then engineer can contact you directly if any problems arise.
</p>
